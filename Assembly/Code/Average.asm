.MODEL SMALL
.STACK 100H

.DATA
ARRAY1 DB  3, 3, 6, 8, 5, 5
SUM DB 0
AVERAGE DB ?

.CODE
MAIN PROC
MOV AX, @DATA
MOV DS, AX

MOV SI, OFFSET ARRAY1
MOV CX, 

PUSH CX   

LOOP1:
MOV DL, [SI]
ADD SUM, DL
INC SI
LOOP LOOP1

POP CX    ;

MOV AL, SUM
MOV AH, 0

MOV BL, CL  

DIV BL

MOV AVERAGE, AL

MOV AH, 4CH
INT 21H

MAIN ENDP
END MAIN
